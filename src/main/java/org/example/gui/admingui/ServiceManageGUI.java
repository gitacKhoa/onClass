    /**
     * @param args the command line arguments
     */

    public static void updateList() {
        model.clear();
        Product.liUpdate();
        for (Product x : Product.pdArr) {
            model.addElement(x);
        }
    }
    
    public static void updateKey (String key){
        model.clear();
        for (Product x: new ProductDAO().searchProductByKeyword(key)) {
            model.addElement(x);
        }
                
    }
