    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        if (JOptionPane.showConfirmDialog(this,"Bạn có chắc muốn xóa?","Xác nhận", JOptionPane.YES_NO_OPTION)== JOptionPane.YES_OPTION) {
        try {
            Product p = customList1.getSelectedValue();
            ProductDAO.deleteProduct(p.getProduct_id());
            Product.liUpdate();
            updateList();
        } catch (NullPointerException e) {
            JOptionPane.showMessageDialog(null, "Bạn chưa chọn dịch vụ hoặc dịch vụ không tồn tại!");
        }
        }
    }//GEN-LAST:event_jButton2ActionPerformed

